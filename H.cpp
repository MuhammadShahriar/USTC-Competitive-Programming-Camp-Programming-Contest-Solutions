int cnt[30];

int main()
{
    string s, ans;
    cin >> s;
    int n = s.size();

    for ( int i = 0; i < n; i++ ) cnt[s[i]-'A']++;

    int d = 0;
    int ch = -1;

    for ( int i = 0; i < 26; i++ ) {
        if ( cnt[i] % 2 == 1 ) {
            d++;
            ch = i;
        }
    }

    if ( d > 1 ) {
        cout << "NO SOLUTION" << endl;
        return 0;
    }

    for ( int i = 0; i < 26; i++ ) {
        int lim = cnt[i] / 2;
        for ( int j = 0; j < lim; j++ ) {
            ans += (i + 'A');
        }
    }

    if( ch != -1 ) ans += ch + 'A';

    for ( int i = 25; i >= 0; i-- ) {
        int lim = cnt[i] / 2;
        for ( int j = 0; j < lim; j++ ) {
            ans += (i + 'A');
        }
    }

    cout << ans << endl;

    return 0;
}
